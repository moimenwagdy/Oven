    keystoreProperties.load(keystorePropertiesFile.inputStream())
}

println(">>> Signing config:")
println("storeFile = ${keystoreProperties.getProperty("storeFile")}")
println("keyAlias = ${keystoreProperties.getProperty("keyAlias")}")

